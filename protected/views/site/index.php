<p>How to find the offset of the wheel? Let&rsquo;s try to figure out on the following example.</p>
<p>1) Put the wheel of the flat surface</p>
<p>2) Measure the width of the wheel (distance between the surface it touches to the upper point)</p>
<p>3) Find the centerline by dividing the width by half (for example, if the width is 9 inches, center of the wheel is &ndash; 4.5 inches)</p>
<p>4) Measure the backspacing (distance from the face of the hub to the top of the wheel), let&rsquo;s say 6 inches.</p>
<p>5) Wheel offset would be the difference between backspacing and center of the wheel (6 inches minus 4.5 inches = 1.5 inches or 40 mm)</p>
<p>6) In this case the hub would be towards the center of the wheel, so it is positive offset 1.5 inches.</p>
<p>Another thing you have to consider is the brake clearance. It is especially important on vehicles with Brembos or big break kits.</p>

